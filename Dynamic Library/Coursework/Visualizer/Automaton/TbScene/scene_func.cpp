namespace dynlib
{
	namespace automaton
	{
        void allocateOnCircle(const int &count, const double &distance,
			TbPoint2d **points, const TbAPoint2d *center,
			const double &firstRadian)
		{
			if(count == 1)
			{
            	points[0]->coordinate(center);
			}
			else
			{
				double radian = firstRadian;
				double dr = 2 * M_PI / count;
				double radius = distance / sin(dr / 2);
				for(int i = 0; i < count; i++)
				{
					double x = radius * cos(radian);
					double y = radius * sin(radian);
					points[i]->coordinate(x, y);
					radian += dr;
                }
			}

        }
    }
}
